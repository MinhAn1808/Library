function displayCSD() {
    let screen = document.getElementById("screen-img");
    screen.style.backgroundImage = "url('img/CSD.jpg')";
    screen.style.display = "block"

}
function displayCSDInfo() {
    let screen = document.getElementById("screen-info");
    screen.innerHTML = "This is a document about data structures and algorithms"
    screen.style.background = "#fac1fa"
    screen.style.fontSize = "50px"
    screen.style.display = "block"
    screen.style.textAlign = "center"
}
function mouseOut() {
    document.getElementById('screen-img').style.display = 'none'
    document.getElementById("screen-info").style.display = "none"
}
